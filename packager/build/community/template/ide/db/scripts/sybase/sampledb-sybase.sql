drop table BOARD_USER;
drop table BOARD_MASTER;
drop table BOARD;

CREATE TABLE BOARD_USER(
  BOARD_USER_ID VARCHAR(20) NOT NULL,
  USER_NAME VARCHAR(50) NOT NULL,
  PASSWORD VARCHAR(10) NOT NULL,
  AGE NUMERIC(3) NULL,
  CELL_PHONE VARCHAR(14) NULL,
  ADDR VARCHAR(100) NULL,
  EMAIL VARCHAR(50) NULL,
  REG_DATE DATE NULL,
  AUTHORITY VARCHAR(50) NOT NULL,
  PRIMARY KEY (BOARD_USER_ID) 
);

CREATE TABLE BOARD_MASTER(
BOARD_MASTER_ID NUMERIC(10) NOT NULL,
TITLE VARCHAR(100) default '' NOT NULL,
DISPLAY_ORDER NUMERIC default 0 NOT NULL,
MODERATED NUMERIC default 0,
PRIMARY KEY (BOARD_MASTER_ID) 
);


CREATE TABLE BOARD(
BOARD_ID NUMERIC(10) NOT NULL,
BOARD_MASTER_ID NUMERIC(10) NOT NULL,
BOARD_NAME VARCHAR(150) default '' NOT NULL,
BOARD_DESC VARCHAR(255) default NULL,
BOARD_ORDER NUMERIC default 1,
BOARD_TOPICS NUMERIC default 0 NOT NULL,
REG_DATE DATE NULL,
PRIMARY KEY (BOARD_ID,BOARD_MASTER_ID), 
FOREIGN KEY(BOARD_MASTER_ID) REFERENCES BOARD_MASTER(BOARD_MASTER_ID)
);