drop table BOARD_USER;
drop table BOARD;
drop table BOARD_MASTER;

CREATE TABLE BOARD_USER(
  USER_ID VARCHAR(20) NOT NULL,
  USER_NAME VARCHAR(50) NOT NULL,
  PASSWORD VARCHAR(10) NOT NULL,
  AGE DECIMAL(3),
  CELL_PHONE VARCHAR(14),
  ADDR VARCHAR(100),
  EMAIL VARCHAR(50),
  REG_DATE DATETIME default GETDATE(),
  AUTHORITY VARCHAR(50) NOT NULL,
  CONSTRAINT PK_BOARD_USER PRIMARY KEY(USER_ID) 
);

CREATE TABLE BOARD_MASTER(
BOARD_MASTER_ID DECIMAL(10),
TITLE VARCHAR(100) default '' NOT NULL,
DISPLAY_ORDER DECIMAL default 0 NOT NULL,
MODERATED DECIMAL default 0,
CONSTRAINT PK_BOARD_MASTER PRIMARY KEY(BOARD_MASTER_ID) 
);


CREATE TABLE BOARD(
BOARD_ID DECIMAL,
BOARD_MASTER_ID DECIMAL(10) NOT NULL,
BOARD_NAME VARCHAR(150) default '' NOT NULL,
BOARD_DESC VARCHAR(255) default NULL,
BOARD_ORDER DECIMAL default 1,
BOARD_TOPICS DECIMAL default 0 NOT NULL,
REG_DATE DATETIME default GETDATE(),
CONSTRAINT PK_BOARD PRIMARY KEY (BOARD_ID,BOARD_MASTER_ID), 
CONSTRAINT FK_BOARD FOREIGN KEY(BOARD_MASTER_ID) REFERENCES BOARD_MASTER(BOARD_MASTER_ID)
);
