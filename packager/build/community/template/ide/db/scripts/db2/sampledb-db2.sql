drop table BOARD_USER;
drop table BOARD;
drop table BOARD_MASTER;

CREATE TABLE BOARD_USER(
  USER_ID VARCHAR(20) NOT NULL,
  USER_NAME VARCHAR(50) NOT NULL,
  PASSWORD VARCHAR(10) NOT NULL,
  AGE DECIMAL(3),
  CELL_PHONE VARCHAR(14),
  ADDR VARCHAR(100),
  EMAIL VARCHAR(50),
  REG_DATE DATE,
  AUTHORITY VARCHAR(50) NOT NULL,
  PRIMARY KEY(USER_ID) 
);

CREATE TABLE BOARD_MASTER(
  BOARD_MASTER_ID DECIMAL(10) NOT NULL,
  TITLE VARCHAR(100) NOT NULL,
  DISPLAY_ORDER NUMERIC(9,0) NOT NULL,
  MODERATED NUMERIC,
  PRIMARY KEY (BOARD_MASTER_ID)
);

CREATE TABLE BOARD(
  BOARD_ID NUMERIC NOT NULL,
  BOARD_MASTER_ID DECIMAL(10) NOT NULL,
  BOARD_NAME VARCHAR(150) NOT NULL,
  BOARD_DESC VARCHAR(255) default NULL,
  BOARD_ORDER NUMERIC default 1,
  BOARD_TOPICS NUMERIC default 0 NOT NULL,
  REG_DATE DATE,
  PRIMARY KEY(BOARD_ID,BOARD_MASTER_ID), 
  FOREIGN KEY(BOARD_MASTER_ID) REFERENCES BOARD_MASTER(BOARD_MASTER_ID)
);
